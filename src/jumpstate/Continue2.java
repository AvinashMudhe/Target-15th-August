package jumpstate;

public class Continue2 {

	public static void main(String[] args) {
		// continue;
		// in this type of jumping statements once the condition meet then it goes to
		// direct iteration means not go to print

		for (int s = 35; s <= 50; s++) {

			if (s == 40) {

			continue;
			}
				System.out.println(s);
				
			}
		}
	

}
