package logicalprogram;

public class DiagonalPattern {

	public static void main(String[] args) {
		//Diagonal Pattern
		
		for(int i=1; i<=6; i++){
			
			for(int j=1; j<=i; j++) {      //For increasing loop i<=6 >>....j<=i
				System.out.print(" ");
			}	
					System.out.println("*");
				}
				System.out.println();
	}

}
