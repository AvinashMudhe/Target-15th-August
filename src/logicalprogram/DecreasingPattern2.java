package logicalprogram;

public class DecreasingPattern2 {

	public static void main(String[] args) {
		// Decreasing Pattern

		for (int i = 0; i <= 5; i++) { // Same for all logical pattern

			for (int j = i; j <= 5; j++) { // For increasing just initialization change, other are same

				System.out.print("*");
			}
			System.out.println(" ");
		}
	}
}
