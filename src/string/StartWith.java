package string;

public class StartWith {

	public static void main(String[] args) {
		
		//startwith
		
		String s="SAMBHAJINAGAR";
		
		System.out.println(s.startsWith("NAGAR"));
		
		System.out.println(s.startsWith("SAMBHAJI"));
		
		System.out.println(s.startsWith("S"));
		
		System.out.println(s.startsWith("BHAJI"));
	}

}
