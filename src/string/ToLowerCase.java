package string;

public class ToLowerCase {

	public static void main(String[] args) {
		
		// to lower case

		String s = "MAHABHARAT";

		String t = "RAMAYANA";

		String u = "BAHUBALI";

		System.out.println(s.toLowerCase());

		System.out.println(t.toLowerCase());

		System.out.println(u.toLowerCase());
	}

}
