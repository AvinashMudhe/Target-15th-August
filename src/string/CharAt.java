package string;

public class CharAt {

	public static void main(String[] args) {
		
		//CharAt--> It returns Char value
		
		String s="Avinash";
		for(int i=s.length()-1;i>=0; i--) {
			
		
		System.out.print(s.charAt(i));
		}
		//System.out.println(s.charAt(2));
		
		//System.out.println(s.charAt(0));
	}

}
